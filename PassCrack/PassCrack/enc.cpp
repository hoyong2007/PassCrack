void oaep_dec(char *passwd_txt, char *passwd_enc)
{
	char tmp[256];
	int i;
	//decrypt(str, pass_len);
	for (i = 0; i<128; i++)
		tmp[i] = passwd_enc[i] ^ key2[i];

	for (i = 0; i < 128; i++) {
		passwd_txt[i] = tmp[i] ^ passwd_enc[i + 128];
		passwd_txt[i + 128] = passwd_enc[i];
	}

	for (i = 0; i < 128; i++) {
		tmp[i] = passwd_txt[i] ^ key1[i];
		tmp[i + 128] = passwd_txt[i];
	}

	for (i = 0; i < 128; i++) {
		passwd_txt[i] = tmp[i] ^ passwd_txt[i + 128];
		passwd_txt[i + 128] = tmp[i + 128];
	}

	return;
}


void oaep_enc(char *passwd_txt, char *passwd_enc)
{
	char random_str[128];
	char tmp[256];
	int i;

	for (i = 0; i<128; i++)
		random_str[i] = rand() % 128;
	//encrypt(str, random_str, pass_len);
	for (i = 0; i<128; i++)
		tmp[i] = random_str[i] ^ key1[i];

	for (i = 0; i < 128; i++) {
		passwd_enc[i] = random_str[i];
		passwd_enc[i + 128] = tmp[i] ^ passwd_txt[i];
	}

	for (i = 0; i < 128; i++) {
		tmp[i] = passwd_enc[i + 128] ^ key2[i];
		tmp[i + 128] = passwd_enc[i + 128];
	}

	for (i = 0; i < 128; i++) {
		passwd_enc[i + 128] = tmp[i] ^ passwd_enc[i];
		passwd_enc[i] = tmp[i + 128];
	}

	return;
}


void custom_table_encode(char *s)
{
	for (int i = 0; i<strlen(s); i++)
	{
		if (s[i] >= '0' && s[i] <= '9')//숫자
			s[i] = (s[i] % 16) + 1;
		else if (s[i] >= 'a' && s[i] <= 'o')//영어 소문자  
			s[i] = (s[i] % 16) + 0x30;
		else if (s[i] >= 'p' && s[i] <= 'z')//영어 소문자  
			s[i] = (s[i] % 16) + 0x40;
		else if (s[i] >= 'A' && s[i] <= 'O')//영어 대문자 
			s[i] = (s[i] % 16) + 0x10;
		else if (s[i] >= 'P' && s[i] <= 'Z')//영어 대문자
			s[i] = (s[i] % 16) + 0x20;
		else if (s[i] == '<' || s[i] == '>' || s[i] == '?') //특수문자 
			s[i] = s[i] % 16;
		else if (s[i] == '!')
			s[i] = 0xb;
		else if (s[i] == '_')
			s[i] = 0xd;
	}
}




